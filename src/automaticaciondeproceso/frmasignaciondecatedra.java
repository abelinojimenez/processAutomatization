    private int getcodigo(String facultad) {
        // retornamos su correspondiente codigo de facultad
        if(facultad.equalsIgnoreCase("Ingenieria en Sistemas")){
            return 1790;
        }else if(facultad.equalsIgnoreCase("Ciencias Juridicas y Sociales")){
            return 1750;
        }else if(facultad.equalsIgnoreCase("Administracion")){
            return 1722;
        }else if(facultad.equalsIgnoreCase("Psicologia Clinica")){
            return 3004;
        }else if(facultad.equalsIgnoreCase("Psicologia Industrial")){
            return 6301;
        }else if(facultad.equalsIgnoreCase("Ingles")){
              return 8026;
        }
    return -1;
    }

    private boolean isvalid() {
        if(tablaasignacion.getRowCount()>0)return true;
        return false;
    }

    private void limpiar() {
      
        while (tablaasignacion.getRowCount()>0) {
            ((DefaultTableModel) tablaasignacion.getModel()).removeRow(0);
            
            }
        idcatedratico=-1;
        nombre="";
        catedra=-1;
        materia="";
        idmateria=-1;
    }
