    private void btnasignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnasignarActionPerformed
     
       try {
           if(isvalid()){
            Conection conec=new Conection();
            
            while (tablaasignacion.getRowCount() > 0) {
                // tomamos la fecha actual para almacenar en la db
                java.util.Date date = new Date();
                java.sql.Date fechaActual = new java.sql.Date(date.getTime()); 
                int codfac=getcodigo(tablaasignacion.getValueAt(0,4).toString());
              conec.insert("insert into asignarcurso(idcatedra,idcatedratico,codfacultad,fecha) values("+tablaasignacion.getValueAt(0,2)+","+tablaasignacion.getValueAt(0,0)+","+codfac+",'"+fechaActual+"')");  
         //   JOptionPane.showMessageDialog(this,"insert into asignarcurso(idcatedra,idcatedratico,codfacultad,fecha) values("+tablaasignacion.getValueAt(0,2)+","+tablaasignacion.getValueAt(0,0)+","+codfac+","+fechaActual+")");
            ((DefaultTableModel) tablaasignacion.getModel()).removeRow(0);
            
            }
            //cerramos la conexion
            conec.closeConection();
           JOptionPane.showMessageDialog(this, "Agregado exitosamente");
           limpiar();
           }else{
                JOptionPane.showMessageDialog(this, "Se debe de seleccionar el un docente y un curso almenos para continuar");
           }
        } catch (SQLException ex) {
            Logger.getLogger(MenuPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnasignarActionPerformed

